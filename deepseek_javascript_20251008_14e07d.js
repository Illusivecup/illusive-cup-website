// –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∏ –±—Ä–∞—É–∑–µ—Ä–æ–º –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö —Ñ—É–Ω–∫—Ü–∏–π
function checkBrowserSupport() {
    const requiredFeatures = [
        'Promise',
        'Map',
        'Set',
        'Object.entries',
        'Array.prototype.includes',
        'String.prototype.includes'
    ];

    const missingFeatures = requiredFeatures.filter(feature => !window[feature]);

    if (missingFeatures.length > 0) {
        throw new Error(`–ë—Ä–∞—É–∑–µ—Ä –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã–µ —Ñ—É–Ω–∫—Ü–∏–∏: ${missingFeatures.join(', ')}`);
    }

    // –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø–æ–¥–¥–µ—Ä–∂–∫–∏ —Å–æ–≤—Ä–µ–º–µ–Ω–Ω—ã—Ö API
    if (!('content' in document.createElement('template'))) {
        throw new Error('–ë—Ä–∞—É–∑–µ—Ä –Ω–µ –ø–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ—Ç HTML templates');
    }

    return true;
}

// –ü–æ–∫–∞–∑–∞—Ç—å —ç–∫—Ä–∞–Ω –∑–∞–≥—Ä—É–∑–∫–∏
function showLoadingScreen() {
    const loadingHTML = `
        <div class="loading-overlay" id="appLoading">
            <div class="loading-content">
                <div class="loading-spinner"></div>
                <p class="loading-text">–ó–∞–≥—Ä—É–∑–∫–∞ Illusive Cup...</p>
            </div>
        </div>
    `;
    document.body.insertAdjacentHTML('beforeend', loadingHTML);
}

// –°–∫—Ä—ã—Ç—å —ç–∫—Ä–∞–Ω –∑–∞–≥—Ä—É–∑–∫–∏
function hideLoadingScreen() {
    const loadingElement = document.getElementById('appLoading');
    if (loadingElement) {
        loadingElement.style.opacity = '0';
        setTimeout(() => {
            loadingElement.remove();
        }, 300);
    }
}

